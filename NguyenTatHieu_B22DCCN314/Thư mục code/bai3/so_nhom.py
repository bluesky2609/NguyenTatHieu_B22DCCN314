# Theo em nên chia thành 4 nhóm 
# 	+ Nhóm cầu thủ phòng ngự: Có chỉ số về phòng ngự cao, nhưng có thể chỉ số tấn công thấp.
# 	+ Nhóm cầu thủ tấn công: Sở hữu các chỉ số liên quan đến ghi bàn, kiến tạo cao.
# 	+ Nhóm cầu thủ trung tuyến: Đảm nhiệm vai trò kết nối, với chỉ số phòng ngự và tấn công trung bình.
# 	+ Nhóm thủ môn: Thường có chỉ số rất khác biệt về các yếu tố bảo vệ khung thành.


# --> Vì giúp chia cầu thủ thành các nhóm với vai trò rõ rệt mà không quá chi tiết đến mức khó phân biệt ý nghĩa giữa các nhóm.


# Nhận xét về Kết quả
# 	Sự phân bố các nhóm: Khi nhóm cầu thủ theo các cụm, có thể nhận thấy sự phân bố tự nhiên giữa các nhóm, ví dụ: nhóm phòng ngự, nhóm tấn công, và nhóm trung gian.
# 	Ý nghĩa của các cụm: Các nhóm phân cụm thường có trung bình các chỉ số khác nhau, thể hiện rõ vai trò hoặc điểm mạnh riêng biệt của từng nhóm.